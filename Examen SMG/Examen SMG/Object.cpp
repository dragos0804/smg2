#include "Object.h"

Object::Object(Shader& shader)
{
    cubeVAO = 0;
    cubeVBO = 0;
    model = glm::mat4();
    model = glm::translate(model, glm::vec3(0.0f, 3.0f, 0.0f));
    model = glm::scale(model, glm::vec3(5.0f, 1.0f, 7.0f));
    model = glm::rotate(model, glm::radians(0.0f), glm::vec3(1.0f, 0.0f, 0.0f));
    shader.SetMat4("model", model);

    position = glm::vec3(0.0f, 3.0f, 0.0f);
    rotation = glm::vec3(0.0f, 0.0f, 0.0f);
    scale = glm::vec3(5.0f, 1.0f, 7.0f);
    angleX = 0.0f;
    angleY = 0.0f;
    angleZ = 0.0f;
    size = 0.0f;

    RenderObject();
}

void Object::ProcessKeyboard(Action action, float deltaTime)
{
    if (action == K_LEFT)
    {
        position.x = position.x - 0.5f * deltaTime;
    }
    if (action == K_RIGHT)
    {
        position.x = position.x + 0.5f * deltaTime;
    }
    if (action == K_BACKWARD)
    {
        position.z = position.z + 0.5f * deltaTime;
    }
    if (action == K_FORWARD)
    {
        position.z = position.z - 0.5f * deltaTime;
    }
    if (action == ROTATE_OX)
    {
        rotation = glm::vec3(angleX, angleY, angleZ);
        angleX = angleX + 0.01f;
    }
    if (action == ROTATE_OY)
    {
        rotation = glm::vec3(angleX, angleY, angleZ);
        angleY = angleY + 0.01f;
    }
    if (action == ROTATE_OZ)
    {
        rotation = glm::vec3(angleX, angleY, angleZ);
        angleZ = angleZ + 0.01f;
    }
    if (action == SCALE_UP)
    {
        scale = glm::vec3(scale.x + size, scale.y + size, scale.z + size);
        size = size + 0.00001f;
    }
    if (action == SCALE_DOWN)
    {
        if (size < 0.0f)
            size = 0.0f;
        scale = glm::vec3(scale.x - size, scale.y - size, scale.z - size);
        size = size - 0.00001f;
    }
}

